#include <iostream>
using namespace std;
// Program to find maximum among 2 numbers
int main(void)
{
    int x, y;

    cout << "Enter 2 Numbers: ";
    cin >> x >> y;

    if (x > y)
    {
        cout << "Max is " << x << endl;
    }
    else
    {
        cout << "Max is " << y << endl;
    }

    return 0;
}

